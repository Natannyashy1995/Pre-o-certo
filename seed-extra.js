module.exports = async function seedExtra(Produto) {
  try {
    const existentes = await Produto.find({}, 'nome ativo');
    const nomesTodos = new Set(existentes.map(p => p.nome.toLowerCase().replace(/\s+/g,' ').trim()));
    const novos = PRODUTOS_EXTRAS.filter(p => !nomesTodos.has(p.nome.toLowerCase().replace(/\s+/g,' ').trim()));
    if (novos.length) {
      await Produto.insertMany(novos, { ordered: false }).catch(()=>{});
      console.log(`âœ… seed-extra: +${novos.length} produtos adicionados`);
    }
    // Reativar inativados
    const inativos = existentes.filter(p => {
      const n = p.nome.toLowerCase().replace(/\s+/g,' ').trim();
      return PRODUTOS_EXTRAS.some(s => s.nome.toLowerCase().replace(/\s+/g,' ').trim() === n) && !p.ativo;
    });
    if (inativos.length) {
      await Produto.updateMany({ _id: { $in: inativos.map(p => p._id) } }, { ativo: true });
      console.log(`âœ… seed-extra: ${inativos.length} produtos reativados`);
    }
    if (!novos.length && !inativos.length) {
      console.log('âœ… seed-extra: jÃ¡ completo');
    }
  } catch(e) { console.error('seed-extra erro:', e.message); }
};
